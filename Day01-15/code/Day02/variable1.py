a = 321
b = 123
print(a + b)
print(a - b)
print(a * b)
print(a / b)
# a // b: 这是整数除法运算，它返回的是除法结果的整数部分，舍去小数部分。a 的值是 321，b 的值是 123，所以 a // b 的结果是 2。
print(a // b)
print(a % b)
# a ** b: 这是幂运算，计算 a 的 b 次幂。a 的值是 321，b 的值是 123，所以 a ** b 的结果是一个非常大的数，它表示 321 的 123 次幂，这个结果将非常大。
print(a ** b)
